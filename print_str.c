#include "printf.h"

int	print_str(char *str)
{
	int	i;

	i = 0 ;
	if (str == NULL)
	{
		i = write(1, "(null)", 6);
		return (i);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		printf("%d" , i);
		i++;
	}
	return (i);
}
